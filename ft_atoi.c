#include "libft.h"

int	ft_atoi(const char *str)
{
	int	rslt;
	int	sign;

	rslt = 0;
	sign = 1;
	while (*str == ' ' || (*str >= '\t' && *str <= '\r'))
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			sign *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		rslt = rslt * 10 + *str - '0';
		str++; 
	}
	return (rslt * sign);
}
